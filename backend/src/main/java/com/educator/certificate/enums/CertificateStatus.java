package com.educator.certificate.enums;

/**
 * Defines the lifecycle state of a Certificate.
 *
 * GENERATED : Certificate record has been created.
 * ISSUED    : Certificate is officially issued to the learner.
 * REVOKED   : Certificate has been revoked (future use).
 * EXPIRED   : Certificate has expired (future use).
 *
 * This enum contains no business logic.
 */
public enum CertificateStatus {

    GENERATED,
    ISSUED,
    REVOKED,
    EXPIRED

}
